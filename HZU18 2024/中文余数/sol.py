def crt(a, n):
    N = 1
    for ni in n:
        N *= ni
    result = 0
    for ai, ni in zip(a, n):
        mi = N // ni
        mi_inv = inverse(mi, ni)
        result += ai * mi * mi_inv
    return result % N

